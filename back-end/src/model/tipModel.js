import db from '../config/database';

export function list(){
    const query = `SELECT * FROM tip`;

    return new Promise((resolve, reject) => {
        db.query(query, (error, result) => {
            if(error) {
                reject(error);
            } else {
                resolve(result);
            }
        })
    })
}

